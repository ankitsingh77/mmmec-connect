package com.quipmate2.dto;

public class Photo {
	private String file;
	private String actionId;
	private String lifeIsFun;
	private String actionOn;
	private String actionBy;
	private String time;

	public String getFile() {
		return file;
	}

	public void setFile(String file) {
		this.file = file;
	}

	public String getActionId() {
		return actionId;
	}

	public void setActionId(String actionId) {
		this.actionId = actionId;
	}

	public String getLifeIsFun() {
		return lifeIsFun;
	}

	public void setLifeIsFun(String lifeIsFun) {
		this.lifeIsFun = lifeIsFun;
	}

	public String getActionOn() {
		return actionOn;
	}

	public void setActionOn(String actionOn) {
		this.actionOn = actionOn;
	}

	public String getActionBy() {
		return actionBy;
	}

	public void setActionBy(String actionBy) {
		this.actionBy = actionBy;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

}
